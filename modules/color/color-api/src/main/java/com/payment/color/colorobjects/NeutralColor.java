package com.payment.color.colorobjects;

public class NeutralColor {

	private String black;
	private String white;
	private String gray1;

	public String getBlack() {
		return black;
	}

	public void setBlack(String black) {
		this.black = black;
	}

	public String getWhite() {
		return white;
	}

	public void setWhite(String white) {
		this.white = white;
	}

	public String getGray1() {
		return gray1;
	}

	public void setGray1(String gray1) {
		this.gray1 = gray1;
	}

	@Override
	public String toString() {
		return "NeutralColor [black=" + black + ", white=" + white + ", gray1=" + gray1 + "]";
	}

}
